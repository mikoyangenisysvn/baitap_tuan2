# Sample array
my_array = [1, 2, 3, 4, 5]

# Value to insert
value_to_insert = 99

# Index where the value will be inserted
insert_index = 2

# Using insert() method to insert the value at the specified index
my_array.insert(insert_index, value_to_insert)

# Display the modified array
print("Updated array:", my_array)
